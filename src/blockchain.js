export const createParticipant = async(name, year, feedback) => {

    //error handling
    if (name.trim() === ""  || year < 0 || year > 5 || feedback.trim() === "") { 
        return {
            success: false,
            status: "❗Please make sure all fields are completed before creating proposal.",
        }
    }
  
    //load smart contract
    window.contract = await new web3.eth.Contract(contractABI, contractAddress);//loadContract();
  
    //set up your Ethereum transaction
    const transactionParameters = {
        to: contractAddress, // Required except during contract publications.
        from: window.ethereum.selectedAddress, // must match user's active address.
        'data': window.contract.methods.createFeedback(name,Number(year),feedback).encodeABI() //make call to NFT smart contract 
    };
  
    //sign transaction via Metamask
    try {
        const txHash = await window.ethereum
            .request({
                method: 'eth_sendTransaction',
                params: [transactionParameters],
            });
        return {
            success: true,
            status: "✅ Check out your transaction on Etherscan: https://kovan.etherscan.io/tx/" + txHash
        }
    } catch (error) {
        return {
            success: false,
            status: "😥 Something went wrong: " + error.message
        }
    }
}
  
